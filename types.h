#ifndef __TYPES_H
#define __TYPES_H

typedef char int8_t;           /*1 byte data*/
typedef unsigned char uint8_t; /*1 byte unsigned data*/

typedef short int16_t;           /*2 byte data*/
typedef unsigned short uint16_t; /*2 byte unsigned data*/

typedef int int32_t;           /*4 byte data*/
typedef unsigned int uint32_t; /*4 byte unsigned data*/

typedef long long int64_t;           /*8 byte data*/
typedef unsigned long long uint64_t; /*8 byte unsigned data*/

#endif
